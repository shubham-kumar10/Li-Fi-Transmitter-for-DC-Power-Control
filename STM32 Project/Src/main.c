static void UART_encode(uint16_t var)
{
	tmp = var;
	for (i = 0; i < 2; i++) 
	{
		encoded[i] = 0;    
		for (j = 0; j < 4; j++) 
		{
			encoded[i] >>= 2;
			if (tmp & 0x01)
					encoded[i] |= 0x40;  
			else
					encoded[i] |= 0x80;  
			tmp >>= 1;
		}
	}
}

